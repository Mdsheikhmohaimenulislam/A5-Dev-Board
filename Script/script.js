document.getElementById('card-btn').addEventListener('click',function(event){
    event.preventDefault();

    const taskAssigend = document.getElementById('task-assigend').innerText;
    const convertTaskAssigend = parseFloat(taskAssigend);


    const increment = 1
    const convertIncrement = parseFloat(increment)

 
    
    
    const addNumber = document.getElementById('add-number').innerText;
    const convertAddNumber = parseFloat(addNumber);

    if(convertTaskAssigend){
        alert("Board Updated Successfully");
        const sum = convertTaskAssigend - convertIncrement;
        document.getElementById('task-assigend').innerText = sum;

        if(convertAddNumber){

            const sum = convertAddNumber + convertIncrement;
            document.getElementById('add-number').innerText = sum;
            

            if(convertAddNumber){

               const time = new Date().toLocaleTimeString();

               const currentTime = document.getElementById('show-sms');
               
               const storTime = document.createElement('storTime');
               
               storTime.innerHTML = `<h3 class="bg-[#EDF1FC] p-2">You have completed the task, Fix Mobile Button Issue  ${time} </h3>`;
               
               currentTime.appendChild(storTime);

            
                document.getElementById('card-btn').disabled = true;
            }
            else{
                alert('Invalid code');
            }
        }
        else{
            alert('Invalid code');
        }
    }
    else{
        alert('Invalid code');
    }
    

    

});


